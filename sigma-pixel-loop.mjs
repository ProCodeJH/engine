#!/usr/bin/env node
// sigma-pixel-loop.mjs — v107 자동 pixelmatch 회귀 + 갭 자동 진단
//
// 자현 ultrathink: 자현 자산 끼운 후 source vs clone 비교. 단순 매치 %
// 가 아니라 "갭의 원인 자동 진단" — Y좌표별 diff intensity → section
// 식별 → 그 section에 부족한 신호 추정 → IMPOSSIBLE.md 자동 갱신.
// 자기 학습 루프 (사이트 클론할수록 엔진 진화).
//
// Strategy:
//   1. Source URL screenshot (3 viewports — mobile/tablet/desktop)
//   2. Clone localhost screenshot (3 viewports)
//   3. pixelmatch viewport별
//   4. > 5% 갭이면 Y-axis diff intensity histogram 추출
//   5. 가장 큰 갭의 Y 좌표 → extracted.sections에서 매칭 → 어느 role section
//   6. 그 section의 신호 부족 진단 (color/font/layout/animation/spacing)
//   7. PIXEL-LOOP-REPORT.md (viewport별 + section별 + 권고)

import fs from "node:fs";
import path from "node:path";
import { spawn, spawnSync } from "node:child_process";
import { fileURLToPath } from "node:url";

const CHROME = process.env.CHROME_PATH || "C:/Program Files/Google/Chrome/Application/chrome.exe";

export async function runPixelLoop(projDir, opts = {}) {
  const port = opts.port || 3000;
  const skipBuild = opts.skipBuild || false;

  const scanPath = path.join(projDir, ".sigma", "scan.json");
  if (!fs.existsSync(scanPath)) {
    return { error: "missing .sigma/scan.json — was this directory generated by nava-sigma.mjs?" };
  }
  const scan = JSON.parse(fs.readFileSync(scanPath, "utf-8"));
  const sourceUrl = scan.url;
  if (!sourceUrl) return { error: "no source URL in scan.json" };

  const puppeteer = (await import("puppeteer-core")).default;

  // Build clone if not built
  if (!skipBuild && !fs.existsSync(path.join(projDir, ".next"))) {
    if (!fs.existsSync(path.join(projDir, "node_modules"))) {
      console.log("  npm install...");
      const inst = spawnSync("npm", ["install", "--silent", "--no-audit", "--no-fund"], {
        cwd: projDir, shell: true, stdio: "inherit",
      });
      if (inst.status !== 0) return { error: "npm install failed" };
    }
    console.log("  npm run build...");
    const build = spawnSync("npm", ["run", "build"], { cwd: projDir, shell: true, stdio: "inherit" });
    if (build.status !== 0) return { error: "build failed" };
  }

  // Start dev server
  console.log("  starting dev server...");
  const dev = spawn("npm", ["run", "dev", "--", "--port", String(port)], {
    cwd: projDir, shell: true, detached: false,
  });
  let serverReady = false;
  dev.stdout?.on("data", (d) => { if (/Local:|ready/.test(d.toString())) serverReady = true; });
  dev.stderr?.on("data", () => {});
  for (let i = 0; i < 60; i++) {
    await new Promise(r => setTimeout(r, 1000));
    if (serverReady) break;
    try {
      const res = await fetch(`http://localhost:${port}/`, { signal: AbortSignal.timeout(2000) });
      if (res.ok) { serverReady = true; break; }
    } catch {}
  }
  if (!serverReady) {
    dev.kill();
    return { error: "dev server did not start within 60s" };
  }

  const browser = await puppeteer.launch({
    headless: "new", executablePath: CHROME,
    args: ["--no-sandbox", "--disable-dev-shm-usage"],
  });

  const VIEWPORTS = [
    { name: "mobile", w: 375, h: 812 },
    { name: "tablet", w: 768, h: 1024 },
    { name: "desktop", w: 1920, h: 1080 },
  ];

  const results = {};
  const loopDir = path.join(projDir, ".pixel-loop");
  fs.mkdirSync(loopDir, { recursive: true });

  for (const vp of VIEWPORTS) {
    try {
      const page = await browser.newPage();
      await page.setViewport({ width: vp.w, height: vp.h });

      // Source screenshot
      await page.goto(sourceUrl, { waitUntil: "networkidle2", timeout: 30000 });
      await new Promise(r => setTimeout(r, 1500));
      const srcShot = await page.screenshot({ fullPage: false });
      fs.writeFileSync(path.join(loopDir, `source-${vp.name}.png`), srcShot);

      // Clone screenshot
      await page.goto(`http://localhost:${port}/`, { waitUntil: "networkidle2", timeout: 30000 });
      await new Promise(r => setTimeout(r, 1500));
      const cloneShot = await page.screenshot({ fullPage: false });
      fs.writeFileSync(path.join(loopDir, `clone-${vp.name}.png`), cloneShot);

      await page.close();

      // Pixelmatch + Y-axis intensity
      const { default: pixelmatch } = await import("pixelmatch");
      const { PNG } = await import("pngjs");
      const a = PNG.sync.read(srcShot);
      const b = PNG.sync.read(cloneShot);

      if (a.width !== b.width || a.height !== b.height) {
        results[vp.name] = { error: `size mismatch ${a.width}x${a.height} vs ${b.width}x${b.height}` };
        continue;
      }

      const diff = new PNG({ width: a.width, height: a.height });
      const mismatched = pixelmatch(a.data, b.data, diff.data, a.width, a.height, { threshold: 0.1, alpha: 0.3 });
      fs.writeFileSync(path.join(loopDir, `diff-${vp.name}.png`), PNG.sync.write(diff));

      const matchPct = +((1 - mismatched / (a.width * a.height)) * 100).toFixed(2);

      // Y-axis intensity histogram (50 buckets)
      const buckets = 50;
      const bucketSize = Math.ceil(a.height / buckets);
      const histogram = new Array(buckets).fill(0);
      for (let y = 0; y < a.height; y++) {
        const b = Math.floor(y / bucketSize);
        let rowDiff = 0;
        for (let x = 0; x < a.width; x++) {
          const i = (y * a.width + x) * 4;
          if (diff.data[i] !== 0 || diff.data[i + 1] !== 0 || diff.data[i + 2] !== 0) rowDiff++;
        }
        histogram[b] += rowDiff;
      }
      const maxBucket = histogram.indexOf(Math.max(...histogram));
      const hotspotY = maxBucket * bucketSize;

      // Match hotspot Y to a section (from extracted.sections)
      const sections = scan.sections || [];
      let hotspotSection = null;
      for (const s of sections) {
        if (s.top !== undefined && s.h !== undefined &&
            hotspotY >= s.top && hotspotY < s.top + s.h) {
          hotspotSection = s;
          break;
        }
      }

      results[vp.name] = {
        matchPct,
        mismatchedPx: mismatched,
        totalPx: a.width * a.height,
        histogram,
        hotspotY,
        hotspotSection: hotspotSection ? { role: hotspotSection.role, idx: hotspotSection.idx, top: hotspotSection.top, h: hotspotSection.h } : null,
      };

      console.log(`  ${vp.name}: ${matchPct}% match, hotspot Y=${hotspotY} (${hotspotSection?.role || "(unmatched)"})`);
    } catch (e) {
      results[vp.name] = { error: String(e.message || e).slice(0, 80) };
    }
  }

  await browser.close();
  dev.kill();

  // Diagnose gaps
  const diagnostics = diagnoseGaps(results, scan);

  const lines = [
    "# PIXEL-LOOP-REPORT",
    "",
    `Generated: ${new Date().toISOString()}`,
    `Source: ${sourceUrl}`,
    `Project: ${projDir}`,
    "",
    "## Match per viewport",
    "",
    "| Viewport | Match | Mismatched px | Hotspot Y | Hotspot section |",
    "|---|---|---|---|---|",
    ...VIEWPORTS.map(vp => {
      const r = results[vp.name];
      if (r?.error) return `| ${vp.name} | — | ${r.error} | — | — |`;
      return `| ${vp.name} | **${r.matchPct}%** | ${r.mismatchedPx?.toLocaleString()} | ${r.hotspotY} | ${r.hotspotSection?.role || "(unmatched)"} |`;
    }),
    "",
    "## 갭 진단 (자동 권고)",
    "",
    diagnostics.length === 0 ? "_(매치 만족스러움 — 추가 작업 불필요)_" :
      diagnostics.map(d => `- **${d.viewport}** ${d.issue}: ${d.recommendation}`).join("\n"),
    "",
    "## 자기 학습 루프",
    "",
    "이 보고서의 권고를 sigma-impossible.mjs의 SOLVABLE_PARTIAL 룰에 추가하면",
    "다음 사이트 클론 시 IMPOSSIBLE.md 정확도 자동 향상.",
    "",
    "## Artifacts",
    "",
    "- `.pixel-loop/source-{viewport}.png` / `clone-{viewport}.png` / `diff-{viewport}.png`",
    "- 이 보고서: `PIXEL-LOOP-REPORT.md`",
  ];
  fs.writeFileSync(path.join(projDir, "PIXEL-LOOP-REPORT.md"), lines.join("\n"));

  return { results, diagnostics };
}

function diagnoseGaps(results, scan) {
  const out = [];
  for (const [vp, r] of Object.entries(results)) {
    if (r.error || r.matchPct >= 95) continue;
    if (r.hotspotSection) {
      const s = r.hotspotSection;
      // Heuristic: which signal class might be missing
      const fullSection = (scan.sections || []).find(x => x.idx === s.idx);
      if (!fullSection?.motions || fullSection.motions.length === 0) {
        out.push({
          viewport: vp,
          issue: `${s.role} section gap (${100 - r.matchPct}%)`,
          recommendation: "no captured motions in this section — likely scroll-driven animation missing. Try v90-1 scrollDrivenAnimations or v82-1 WAAPI sync groups.",
        });
      } else if (!fullSection.images || fullSection.images.length === 0) {
        out.push({
          viewport: vp,
          issue: `${s.role} section gap (${100 - r.matchPct}%)`,
          recommendation: "no captured images — HANDOFF slot likely empty. Run sigma-asset-inject.mjs with user images.",
        });
      } else {
        out.push({
          viewport: vp,
          issue: `${s.role} section gap (${100 - r.matchPct}%)`,
          recommendation: "section captured but emit gap — check v74 DOM Mirror for this section, or v85-2 element-rule bindings.",
        });
      }
    } else {
      out.push({
        viewport: vp,
        issue: `unmatched hotspot Y=${r.hotspotY} (${100 - r.matchPct}% gap)`,
        recommendation: "hotspot not in any captured section — page may have lazy-load or below-fold dynamic content. Try v99-1 multi-state or v100-1 multi-viewport reload.",
      });
    }
  }
  return out;
}

const isMain = import.meta.url === `file://${process.argv[1]}` ||
               (process.argv[1] && fileURLToPath(import.meta.url) === path.resolve(process.argv[1]));
if (isMain) {
  const projDir = path.resolve(process.argv[2] || "");
  if (!projDir || !fs.existsSync(projDir)) {
    console.error("usage: node sigma-pixel-loop.mjs <projectDir> [--port 3000] [--skip-build]");
    process.exit(1);
  }
  const portIdx = process.argv.indexOf("--port");
  const port = portIdx >= 0 ? parseInt(process.argv[portIdx + 1], 10) : 3000;
  const skipBuild = process.argv.includes("--skip-build");
  runPixelLoop(projDir, { port, skipBuild }).then(r => {
    if (r.error) {
      console.error(`[pixel-loop] error: ${r.error}`);
      process.exit(1);
    }
    console.log(`[pixel-loop] done → PIXEL-LOOP-REPORT.md`);
  });
}
